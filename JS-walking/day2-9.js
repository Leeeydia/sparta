function getName(person) {
  return person.name;
}

// 예시 출력
const name1 = getName({ name: 'Alice', age: 25 });
console.log(name1); // 출력: Alice

const name2 = getName({ name: 'Bob', age: 30 });
console.log(name2); // 출력: Bob





